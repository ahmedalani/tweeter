function  createtweetElement (tweet) {
  let $newTweet = $("<article>").addClass("entire-tweet");
  let $header = $(`
    <header>
      <img src="${tweet.user.avatars.small}">
      <h1>${tweet.user.name}</h1>
      <p>${tweet.user.handle}</p>
    </header>
  `);
  $newTweet.append($header);

  let $content = $(`<div>${tweet.content.text}</div>`).addClass("content");
  $newTweet.append($content);

  let $footer = $(`
    <footer>
      <div class="time-stamp"><a href="#">${tweet.created_at}</a></div>
      <div class="tweet-actions">
        <i class="fa fa-flag" aria-hidden="true"></i>
        <i class="fa fa-retweet" aria-hidden="true"></i>
        <i class="fa fa-heart" aria-hidden="true"></i>
      </div>
    </footer>
  `);
  $newTweet.append($footer);
  return $newTweet;
}

